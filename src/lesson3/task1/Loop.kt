 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */

//solution for n=50 (+loop)
fun fib(n: Int): Long {
    var fibp: Long = 1
    var fibpp: Long = 1
    var fibn: Long = 1
    if (n == 1 || n == 2) 1
    else if (n > 2) {
        for (i in 3..n) {
            fibn = fibp + fibpp
            fibpp = fibp
            fibp = fibn
        }
    }
    return fibn
}
